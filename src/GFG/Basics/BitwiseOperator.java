package GFG.Basics;

public class BitwiseOperator {
    public static void main(String[] args) {

         int x=3;
         int y=6;
//         And operator
         System.out.println(x&y);
         //OR operator
        System.out.println(x|y);
        //XOR operator
        System.out.println(x^y);
    }
}
